class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        return array_api.broadcast_to(a, self.shape)

    def gradient(self, out_grad, node):
        ori_shape = node.inputs[0].shape
        shrink_dims = [i for i in range(len(self.shape))]
        # Broadcast_to默认的功能是降维
        # 从后往前进行匹配，如果不同就append相应的维度，这时候会出现两种情况，第一种情况两个维度都有，但不相同，第二种情况，两个Tensor长度不一致
        for i, (ori, cur) in enumerate(zip(reversed(ori_shape), reversed(self.shape))):
            if ori == cur:
                shrink_dims[len(self.shape) - i - 1] = -1
        shrink_dims = tuple(filter(lambda x: x >= 0, shrink_dims))
        # 多出去的维度就相当于batch，总维度一样之后进行reshape,
        return out_grad.sum(shrink_dims).reshape(ori_shape)


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.sum(a,axis=self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        # print('-----------------------------------------')
        # print(node.inputs[0].shape,out_grad.shape)
        # print(self.axes)
        origin_shape = list(node.inputs[0].shape)
        if self.axes is None:
            origin_shape = [1] * len(origin_shape) # 如果axes为None，则是一个常数，则将summation reshape到之前[1,1,1,1]一样的大小
        else :
            for axe in self.axes:
                origin_shape[axe] = 1
        return broadcast_to(out_grad.reshape(tuple(origin_shape)),node.inputs[0].shape)
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return array_api.matmul(a,b)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        lhs,rhs = node.inputs
        # 求导之后为了满足原来的形状，需要对原矩阵进行装置，交换两者的顺序
        lgrad,rgrad = matmul(out_grad,transpose(rhs)),matmul(transpose(lhs),out_grad)
        # 如果出现很多个batch要把batch的梯度进行累加
        if len(lhs.shape) < len(lgrad.shape):
            lgrad = lgrad.sum(tuple([i for i in range(len(lgrad.shape) - len(lhs.shape))]))
        if len(rhs.shape) < len(rgrad.shape):
            rgrad = rgrad.sum(tuple([i for i in range(len(rgrad.shape) - len(rhs.shape))]))
        return lgrad,rgrad
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


